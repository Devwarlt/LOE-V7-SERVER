		public override int Size => 31;

		public override void RenderSetPiece(World world, IntPoint pos)
		{
			EmbeddedData dat = world.Manager.GameData;
			for (int x = 0; x < Size; x++)
			{
				for (int y = 0; y < Size; y++)
				{
					int r = rand.Next(Ground.Length);
					if (SetPiece[y, x] == 1)
					{
						WmapTile tile = world.Map[x + pos.X, y + pos.Y].Clone();
						tile.TileId = dat.IdToTileType[Ground[r]];
						tile.ObjType = 0;
						world.Map[x + pos.X, y + pos.Y] = tile;
					}
					else if (SetPiece[y, x] == 2)
					{
						WmapTile tile = world.Map[x + pos.X, y + pos.Y].Clone();
						tile.TileId = dat.IdToTileType[Lava];
						tile.ObjType = 0;
						world.Map[x + pos.X, y + pos.Y] = tile;
					}
					else if (SetPiece[y, x] == 3)
					{
						WmapTile tile = world.Map[x + pos.X, y + pos.Y].Clone();
						tile.TileId = dat.IdToTileType[Ground[1]];
						tile.ObjType = 0;
						world.Map[x + pos.X, y + pos.Y] = tile;
					}
					else if (SetPiece[y, x] == 4)
					{
						WmapTile tile = world.Map[x + pos.X, y + pos.Y].Clone();
						tile.TileId = dat.IdToTileType[Ground[r]];
						tile.ObjType = 0;
						world.Map[x + pos.X, y + pos.Y] = tile;
					}
				}
			}
		}
